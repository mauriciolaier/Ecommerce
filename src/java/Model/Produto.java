package Model;

/**
 *
 * @author Thallys pc
 */
public class Produto {
    
    private int preco;
    private int id;
    private String nome;

    public Produto() {
    }

    public int getPreco() {
        return preco;
    }

    public void setPreco(int preco) {
        this.preco = preco;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
    
}
